def deactivate_folder(folder_id):
    # –§—É–Ω–∫—Ü–∏—è –¥–µ–∫–∞—Ç–∏–≤–∏—Ä—É–µ—Ç –ø–∞–ø–∫—É –∏ —É–¥–∞–ª—è–µ—Ç –µ–≥–æ –∫–∞–Ω–∞–ª—ã –∏–∑ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö
    folder_returned_message = db_requests.change_folder_status(folder_id, 'disable')

    # –ü–æ–ª—É—á–µ–Ω–∏–µ —Å–ø–∏—Å–∫–∞ –∫–∞–Ω–∞–ª–æ–≤ –¥–ª—è –¥–∞–ª—å–Ω–µ–π—à–∏—Ö –¥–µ–π—Å—Ç–≤–∏–π
