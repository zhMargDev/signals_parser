def check_folders(folders):
    # –§—É–Ω–∫—Ü–∏—è –ø—Ä–æ–≤–µ—Ä—è–µ—Ç –Ω–∞–ª–∏—á–∏–µ –ø–∞–ø–æ–∫ –≤ –±–∞–∑–µ –¥–∞–Ω–Ω—ã–∑, –ø—Ä–∏ –∏—Ö –æ—Ç—Å—É—Ç—Å—Ç–≤–∏–∏ –¥–æ–±–∞–≤–ª—è–µ—Ç –Ω–∞–∑–≤–∞–Ω–∏—è –≤ –æ—Ç–¥–µ–ª—å–Ω—É—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é
    # –¢–µ –∫–æ—Ç–æ—Ä—ã–µ –∑–∞—Ä–µ–≥–µ—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω—ã –≤ –±–¥ –Ω–æ –∏—Ö –Ω–µ—Ç—É –≤ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–Ω–æ–π —Ñ–æ—Ä–º–µ, –¥–æ–±–∞–≤–∏—Ç –≤ –æ—Ç–¥–µ–ª—å–Ω—É—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é
    new_folders = [] # –ú–∞—Å—Å–∏–≤ –¥–ª—è –ø–∞–ø–æ–∫ –∫–æ—Ç–æ—Ä—ã–µ –Ω–∞–¥–æ –¥–æ–±–∞–≤–∏—Ç—å
    folders_for_deleting = [] # –ú–∞—Å—Å–∏–≤ –¥–ª—è –ø–∞–ø–æ–∫ –∫–æ—Ç–æ—Ä—ã–µ –Ω–∞–¥–æ —É–¥–∞–ª–∏—Ç—å

    # –ü–æ–ª—É—á–∏—Ç—å –≤—Å–µ –ø–∞–ø–∫–∏ –∏–∑ –±–¥
    db_folders = db_requests.select_all_folders()

    if len(db_folders) == 0:
        # –ï—Å–ª–∏ —Ç–∞–±–ª–∏—Ü–∞ –ø–∞–ø–æ–∫ –ø—É—Å—Ç–∞—è —Ç–æ –≤—ã–∑–≤–∞—Ç—å —Ñ—É–∫–Ω—Ü–∏—é –¥–ª—è –¥–æ–±–∞–≤–ª–µ–Ω–∏—è –≤—Å–µ—Ö –ø–∞–ø–æ–∫
        added_folders = db_requests.add_folders(folders)
        if '–û—à–∏–±–∫–∞' not in added_folders:
            # –ï—Å–ª–∏ –æ—à–∏–±–∫–∏ –Ω–µ—Ç—É –≤ –≤–æ–∑–≤—Ä–∞—â–µ–Ω–Ω–æ–º —Ç–µ–∫—Å—Ç–µ —Ç–æ –ø–µ—Ä–µ–¥–∞—Ç—å –¥–∞–Ω–Ω—ã–µ –≤—Å–µ—Ö –ø–∞–ø–æ–∫ –º–∞—Å—Å–∏–≤—É
            new_folders = folders

            # –í–µ—Ä–Ω—É—Ç—å –¥–æ–±–∞–≤–ª–µ–Ω–Ω—ã–µ –ø–∞–ø–∫–∏ —Å –ø—É—Å—Ç—ã–º –º–∞—Å—Å–∏–≤–æ–º –¥–ª—è —É–¥–∞–ª—ë–Ω–Ω—ã—Ö
            # –í–æ–∑–≤—Ä–∞—â–∞–µ–Ω–∏–µ –ø—Ä–æ–ø—É—Å–∫–∞–µ—Ç –æ—Å—Ç–∞–ª—å–Ω–æ–π –∫–æ–¥, —Å–∫–æ—Ä–µ–Ω–∏–µ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
            return {'new_folders':new_folders, 'deleted_folders':folders_for_deleting}
        else:
            return '–û—à–∏–±–∫–∞, —á—Ç–æ —Ç–æ –ø–æ—à–ª–æ –Ω–µ —Ç–∞–∫, –ø—Ä–æ–ø–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑.'
    else:
        # –ï—Å–ª–∏ —Ç–∞–±–ª–∏—Ü–∞ –ø–∞–ø–æ–∫ –∏–∑ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –Ω–µ –ø—É—Å—Ç–∞

        # –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–∞–∫–∏–µ –ø–∞–ø–∫–∏ –Ω–µ –±—ã–ª–∏ –∑–∞—Ä–µ–≥–µ—Å—Ç—Ä—Ä–æ–≤–∞–Ω—ã
        # –ü—Ä–æ–≤–µ—Ä–∫–∞, –µ—Å–ª–∏ –ª–∏ –ø–∞–ø–∫–∞ –≤ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö
        for folder in folders:
            flag = True # –§–ª–∞–≥ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏, —Å—Ç–∞–Ω–æ–≤–∏—Ç—Å—è False –µ—Å–ª–∏ –Ω–∞–∑–≤–∞–Ω–∏–µ –ø–∞–ø–∫–∏ –Ω–∞–π–¥–µ–Ω–æ –≤ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö
            for db_folder in db_folders:
                if folder['title'] == db_folder[2]:
                    flag = False
                    break
            if flag:
                new_folders.append(folder)
        
        # –î–æ–±–∞–≤–∏—Ç—å –≤ –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö –Ω–æ–≤—ã–µ –ø–∞–ø–∫–∏
        added_folders = db_requests.add_folders(new_folders)

        if '–û—à–∏–±–∫–∞' in added_folders:
            # –í–µ—Ä–Ω—É—Ç—å —Ç–µ–∫—Å—Ç –æ—à–∏–±–∫–∏, –µ—Å–ª–∏ –≤—ã—à–ª–∞ –æ—à–∏–±–∫–∞
            return '–û—à–∏–±–∫–∞, —á—Ç–æ —Ç–æ –ø–æ—à–ª–æ –Ω–µ —Ç–∞–∫, –ø—Ä–æ–ø–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑.'
        # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –Ω–∞–ª–∏—á–∏–µ –ø–∞–ø–∫–∏ –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è
        for db_folder in db_folders:
            flag = False # –§–ª–∞–≥ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø–∞–ø–∫–∏, –µ—Å–ª–∏ –æ–Ω–∞ —Å—Ç–∞–Ω–æ–≤–∏—Ç—å—Å—è True —Ç–æ –ø–∞–ø–∫–∏ –µ—Å—Ç—å –∏ –≤ —Ç–≥ –∫–∞–Ω–∞–ª–µ –∏ –≤ –±–¥
            for folder in folders:
                if db_folder[2] == folder['title']:
                    flag = True
                    break
            if flag == False: # –†–∞—Å—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞—Ç—å –ø–∞–ø–∫–∏ –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è, –∫–æ—Ç–æ—Ä—ã–µ –Ω–µ –±—ã–ª–æ –ø–æ–ª—É—á–µ–Ω–æ –∏ —Ç–≥
                folders_for_deleting.append(db_folder)
        # –£–¥–∞–ª–∏—Ç—å –ø–∞–ø–∫–∏ –∫–æ—Ç–æ—Ä—ã–µ –Ω–µ –±—ã–ª–∏ –ø–æ–ª—É—á–µ–Ω—ã –∏–∑ —Ç–≥
        delete_folders = db_requests.delete_folders(folders_for_deleting)

        if '–û—à–∏–±–∫–∞' in delete_folders:
            # –í–µ—Ä–Ω—É—Ç—å —Ç–µ–∫—Å—Ç –æ—à–∏–±–∫–∏, –µ—Å–ª–∏ –≤—ã—à–ª–∞ –æ—à–∏–±–∫–∞
            return '–û—à–∏–±–∫–∞, —á—Ç–æ —Ç–æ –ø–æ—à–ª–æ –Ω–µ —Ç–∞–∫, –ø—Ä–æ–ø–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑.'
        else:
            # –í–µ—Ä–Ω—É—Ç—å –¥–æ–±–∞–≤–ª–µ–Ω–Ω—ã–µ –∏ —É–¥–∞–ª—ë–Ω–Ω—ã–µ –ø–∞–ø–∫–∏
            return {'new_folders':new_folders, 'deleted_folders':folders_for_deleting}

